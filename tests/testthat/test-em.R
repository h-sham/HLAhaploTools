test_that("enumerate_diplotypes works", {
  prep <- prepare_test_data()

  # Test with first sample
  first_sample <- prep$genotypes[1, , drop = FALSE]
  diplotypes <- enumerate_diplotypes(first_sample, quiet = TRUE)

  # Check basic structure
  expect_true(is.list(diplotypes))
  expect_equal(length(diplotypes), 2) # 2^(n-1) where n=2 loci
  # Replace sapply() with vapply() here:
  expect_true(all(vapply(diplotypes, function(d) all(c("hap1", "hap2") %in% names(d)), logical(1))))


  # Check that all alleles are present across all diplotypes
  all_haps <- unlist(lapply(diplotypes, function(d) c(d$hap1, d$hap2)))
  expect_true(all(c("A*01:01", "A*02:01", "B*07:02", "B*08:01") %in%
    unlist(strsplit(paste(all_haps, collapse = " "), "[^A-Z0-9*:]+"))))

  # Check that we have both possible combinations
  patterns <- c(
    # A*01:01 and B*07:02 in same haplotype
    vapply(diplotypes, function(d) {
      grepl("A\\*01:01", d$hap1) && grepl("B\\*07:02", d$hap1) ||
        grepl("A\\*01:01", d$hap2) && grepl("B\\*07:02", d$hap2)
    }, logical(1)),
    # A*02:01 and B*08:01 in same haplotype
    vapply(diplotypes, function(d) {
      grepl("A\\*02:01", d$hap1) && grepl("B\\*08:01", d$hap1) ||
        grepl("A\\*02:01", d$hap2) && grepl("B\\*08:01", d$hap2)
    }, logical(1))
  )
  expect_true(any(patterns))
})

test_that("compute_posteriors works", {
  # Set up sample diplotypes
  diplotypes <- list(
    list(
      list(hap1 = "A*01:01~B*07:02", hap2 = "A*02:01~B*08:01"),
      list(hap1 = "A*01:01~B*08:01", hap2 = "A*02:01~B*07:02")
    )
  )

  # Set frequencies where first combination is more common
  freqs <- c(0.4, 0.3, 0.2, 0.1)
  names(freqs) <- c("A*01:01~B*07:02", "A*02:01~B*08:01", "A*01:01~B*08:01", "A*02:01~B*07:02")

  # Compute posteriors
  posteriors <- compute_posteriors(diplotypes, freqs, quiet = TRUE)

  # Check structure
  expect_equal(length(posteriors), length(diplotypes))
  expect_equal(length(posteriors[[1]]), 2)

  # First diplotype should have higher probability (0.4*0.3 vs 0.2*0.1)
  # P(diplotype1) = 0.4*0.3/norm, P(diplotype2) = 0.2*0.1/norm
  # norm = 0.4*0.3 + 0.2*0.1
  # Expect diplotype1 to have higher probability
  expect_true(posteriors[[1]][1] > posteriors[[1]][2])

  # Posteriors should sum to 1
  expect_equal(sum(posteriors[[1]]), 1, tolerance = 1e-6)
})

test_that("run_em_algorithm works with synthetic data", {
  prep <- prepare_test_data()

  # Enumerate diplotypes for each sample
  all_diplotypes <- list()
  for (i in seq_len(nrow(prep$genotypes))) {
    all_diplotypes[[i]] <- enumerate_diplotypes(prep$genotypes[i, , drop = FALSE], quiet = TRUE)
  }

  # Run EM algorithm with limited iterations for testing
  freqs <- run_em_algorithm(
    all_diplotypes,
    max_iter = 10,
    epsilon = 1e-4,
    quiet = TRUE
  )

  # Check basic properties
  expect_true(is.numeric(freqs))
  expect_true(all(freqs >= 0))
  expect_true(all(freqs <= 1))
  expect_equal(sum(freqs), 1, tolerance = 1e-6)
  expect_true(!is.null(names(freqs)))

  # Check convergence info
  expect_false(is.null(attr(freqs, "converged")))
  expect_false(is.null(attr(freqs, "iterations")))

  # The number of unique haplotypes should be reasonable
  # With 5 samples, 2 loci, 2 alleles each, max would be 2*2 = 4 haplotypes
  expect_true(length(freqs) <= 4 * 4) # Upper bound estimate
})

test_that("infer_haplotypes works with synthetic data", {
  # Create test data
  test_data <- create_mini_test_data()

  # Run the full inference pipeline
  results <- infer_haplotypes(
    df = test_data,
    loci = c("A", "B"),
    parallel = FALSE,
    quiet = TRUE,
    isFamily = FALSE # Explicitly set to FALSE to avoid warning
  )

  # Check results structure
  expect_true(is.list(results))
  expect_true(all(c(
    "haplotype_frequencies",
    "top_diplotypes",
    "posteriors",
    "loci_used",
    "convergence",
    "iterations"
  ) %in% names(results)))

  # Check haplotype frequencies
  hap_freqs <- results$haplotype_frequencies$frequency
  expect_equal(sum(hap_freqs), 1, tolerance = 1e-6)

  # Check that all samples have a top diplotype assigned
  expect_equal(nrow(results$top_diplotypes), 5)

  # Check that loci were correctly identified
  expect_equal(sort(results$loci_used), c("A", "B"))
})
