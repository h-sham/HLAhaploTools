#' Create Minimal Synthetic HLA Genotype Test Data
#'
#' Generates a small hard-coded dataset representing HLA genotype information
#' across two loci (A and B) for five individuals. The data is intended for
#' use in unit tests of haplotype inference functions.
#'
#' @return A data.frame with columns: ID, A_1, A_2, B_1, B_2
#' @examples
#' create_mini_test_data()
#' @keywords internal
create_mini_test_data <- function() {
  test_data <- data.frame(
    ID = c("S1", "S2", "S3", "S4", "S5"),
    A_1 = c("A*01:01", "A*02:01", "A*03:01", "A*01:01", "A*02:01"),
    A_2 = c("A*02:01", "A*03:01", "A*24:02", "A*11:01", "A*24:02"),
    B_1 = c("B*07:02", "B*08:01", "B*15:01", "B*07:02", "B*35:01"),
    B_2 = c("B*08:01", "B*44:02", "B*35:01", "B*44:02", "B*44:02"),
    stringsAsFactors = FALSE
  )
  return(test_data)
}

#' Prepare Synthetic Genotype Data for Testing
#'
#' Takes test data generated by `create_mini_test_data()` and formats it
#' into genotype strings (e.g., A_1/A_2) for two loci. Also returns a list of loci.
#'
#' @return A list with elements:
#' \describe{
#'   \item{test_data}{original expanded genotype data}
#'   \item{genotypes}{collapsed per-locus genotype strings}
#'   \item{loci}{character vector of included loci}
#' }
#' @examples
#' prepare_test_data()
#' @keywords internal
prepare_test_data <- function() {
  test_data <- create_mini_test_data()
  loci <- c("A", "B")
  genotypes <- data.frame(
    ID = test_data$ID,
    A = paste(test_data$A_1, test_data$A_2, sep = "/"),
    B = paste(test_data$B_1, test_data$B_2, sep = "/"),
    stringsAsFactors = FALSE
  )
  return(list(
    test_data = test_data,
    genotypes = genotypes,
    loci = loci
  ))
}

